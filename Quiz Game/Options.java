public class Options
{
	private int id;
	private String option;
	//Getter and Setter for options
	public void setId(int id)
	{
		this.id=id;
	}
	public int getId()
	{
		return id;
	}
	public void setOption(String options)
	{
		this.option=option;
	}
	public String getOption()
	{
		return option;
	}
}